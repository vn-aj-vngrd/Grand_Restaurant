    private void orderTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_orderTableMouseClicked
        // TODO add your handling code here:

        DefaultTableModel m = (DefaultTableModel) orderTable.getModel();
        int sri = orderTable.getSelectedRow();

        orderidField.setText((String) m.getValueAt(sri, 0).toString());
        cusidField.setText((String) m.getValueAt(sri, 1).toString());
        menucodeField.setText((String) m.getValueAt(sri, 2).toString());
        empidField.setText((String) m.getValueAt(sri, 3).toString());
        statusField.getEditor().setItem((String) m.getValueAt(sri, 4).toString());
        dateField.setText((String) m.getValueAt(sri, 5).toString());
        timeField.setText((String) m.getValueAt(sri, 6).toString());
    }//GEN-LAST:event_orderTableMouseClicked

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        int input = JOptionPane.showConfirmDialog(null,
                "Do you want to delete customer information?", "Grand Restaurant Confirmation", JOptionPane.YES_NO_CANCEL_OPTION);
        if (input == 0) {
            int res = ac.deleteOrder(orderidField);
            if (res > 0) {
                JOptionPane.showMessageDialog(this, "Order is Deleted Successfully.");
                orderidField.setText("");
                cusidField.setText("");
                menucodeField.setText("");
                empidField.setText("");
                dateField.setText("");
                timeField.setText("");
                ac.fetchOrder(orderTable);
            } else {
                JOptionPane.showMessageDialog(this, "Failed to Delete Order.");
            }
        }
    }//GEN-LAST:event_deleteActionPerformed

