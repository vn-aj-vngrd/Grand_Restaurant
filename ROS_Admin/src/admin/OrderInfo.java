    private void deleteAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteAllActionPerformed
        // TODO add your handling code here:
        int input = JOptionPane.showConfirmDialog(null,
                "Do you want to clear all order information?", "Grand Restaurant Confirmation", JOptionPane.YES_NO_CANCEL_OPTION);
        if (input == 0) {
            int res = ac.deleteAll("orders", 0);
            ac.resetAI("orders");
            if (res > 0) {
                JOptionPane.showMessageDialog(this, "Order Information is Cleared.");
                orderidField.setText("");
                cusidField.setText("");
                menucodeField.setText("");
                empidField.setText("");
                dateField.setText("");
                timeField.setText("");
                ac.fetchOrder(orderTable);
            } else {
                JOptionPane.showMessageDialog(this, "Failed to Clear Order Information.");
            }
        }
    }//GEN-LAST:event_deleteAllActionPerformed

