    private void deleteAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteAllActionPerformed
        // TODO add your handling code here:
        int input = JOptionPane.showConfirmDialog(null,
                "Do you want to clear all customer information?", "Grand Restaurant Confirmation", JOptionPane.YES_NO_CANCEL_OPTION);
        if (input == 0) {
            int res = ac.deleteAll("customers", 0);
            ac.resetAI("customers");
            if (res > 0) {
                JOptionPane.showMessageDialog(this, "Customer Information is Cleared.");
                idField.setText("");
                ac.fetchCustomer(cusTable);
            } else {
                JOptionPane.showMessageDialog(this, "Failed to Clear Employee Information.");
            }
        }
    }//GEN-LAST:event_deleteAllActionPerformed

