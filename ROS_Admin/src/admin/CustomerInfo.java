    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        int input = JOptionPane.showConfirmDialog(null,
                "Do you want to delete customer?", "Grand Restaurant Confirmation", JOptionPane.YES_NO_CANCEL_OPTION);
        if (input == 0) {
            int res = ac.deleteCustomer(idField);
            if (res > 0) {
                JOptionPane.showMessageDialog(this, "Customer is Deleted Successfully.");
                idField.setText("");
                ac.fetchCustomer(cusTable);
            } else {
                JOptionPane.showMessageDialog(this, "Failed to Delete Customer.");
            }
        }
    }//GEN-LAST:event_deleteActionPerformed

